/**
 * a + b
 * @param {Number|String} a number a
 * @param {Number|String} b number b 
 * @returns {Number}
 */
export function plus(a, b) {
    return a + b
}

/**
 * a - b
 * @param {Number|String} a number a
 * @param {Number|String} b number b 
 * @returns {Number}
 */
export function minus(a, b) {
    return a - b
}